// Test with a large number of elements
TEST_F(SkipListTest_479, LargeNumberOfElements_479) {
  const int N = 10000;
  std::set<int> keys;
  std::mt19937 rng(42);  // deterministic seed
  
  for (int i = 0; i < N; i++) {
    int key = rng() % 100000;
    keys.insert(key);
    list_->Insert(key);
  }
  
  for (int key : keys) {
    EXPECT_TRUE(list_->Contains(key));
  }
  
  // Check some values that are very unlikely to have been inserted
  // (we use negative values which won't be generated by rng() % 100000)
  for (int i = -100; i < -1; i++) {
    EXPECT_FALSE(list_->Contains(i));
  }
}

// Test boundary values for int
TEST_F(SkipListTest_479, BoundaryValues_479) {
  list_->Insert(INT_MIN + 1);  // Avoid INT_MIN if it causes issues with the sentinel
  list_->Insert(INT_MAX);
  list_->Insert(0);

  EXPECT_TRUE(list_->Contains(INT_MIN + 1));
  EXPECT_TRUE(list_->Contains(INT_MAX));
  EXPECT_TRUE(list_->Contains(0));
  EXPECT_FALSE(list_->Contains(INT_MAX - 1));
  EXPECT_FALSE(list_->Contains(INT_MIN + 2));
}

