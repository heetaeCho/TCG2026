// Simple comparator for int keys
struct IntComparator {
  int operator()(const int& a, const int& b) const {
    if (a < b) return -1;
    if (a > b) return +1;
    return 0;
  }
};

typedef SkipList<int, IntComparator> IntSkipList;

class SkipListTest_479 : public ::testing::Test {
 protected:
  void SetUp() override {
    arena_ = new Arena();
    list_ = new IntSkipList(IntComparator(), arena_);
  }

  void TearDown() override {
    delete list_;
    delete arena_;
  }

  Arena* arena_;
  IntSkipList* list_;
};

// Test that a newly created skip list does not contain any elements
TEST_F(SkipListTest_479, EmptyListContainsNothing_479) {
  EXPECT_FALSE(list_->Contains(0));
  EXPECT_FALSE(list_->Contains(1));
  EXPECT_FALSE(list_->Contains(-1));
  EXPECT_FALSE(list_->Contains(100));
  EXPECT_FALSE(list_->Contains(INT_MIN));
  EXPECT_FALSE(list_->Contains(INT_MAX));
}

// Test inserting a single element and verifying it can be found
TEST_F(SkipListTest_479, InsertSingleElement_479) {
  list_->Insert(42);
  EXPECT_TRUE(list_->Contains(42));
  EXPECT_FALSE(list_->Contains(41));
  EXPECT_FALSE(list_->Contains(43));
}

// Test inserting multiple elements and verifying all are found
TEST_F(SkipListTest_479, InsertMultipleElements_479) {
  list_->Insert(10);
  list_->Insert(20);
  list_->Insert(30);
  
  EXPECT_TRUE(list_->Contains(10));
  EXPECT_TRUE(list_->Contains(20));
  EXPECT_TRUE(list_->Contains(30));
  EXPECT_FALSE(list_->Contains(15));
  EXPECT_FALSE(list_->Contains(25));
  EXPECT_FALSE(list_->Contains(5));
  EXPECT_FALSE(list_->Contains(35));
}

