// Test large sequential insert and iterate
TEST_F(SkipListTest_479, LargeSequentialInsertAndIterate_479) {
  const int N = 1000;
  for (int i = 0; i < N; i++) {
    list_->Insert(i);
  }
  
  IntSkipList::Iterator iter(list_);
  iter.SeekToFirst();
  int count = 0;
  int prev = -1;
  while (iter.Valid()) {
    EXPECT_GT(iter.key(), prev);
    prev = iter.key();
    iter.Next();
    count++;
  }
  EXPECT_EQ(N, count);
}

