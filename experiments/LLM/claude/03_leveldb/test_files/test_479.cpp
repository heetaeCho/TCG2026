// Test iterator forward traversal produces sorted order
TEST_F(SkipListTest_479, IteratorForwardTraversal_479) {
  std::vector<int> values = {50, 10, 30, 20, 40};
  for (int v : values) {
    list_->Insert(v);
  }
  std::sort(values.begin(), values.end());

  IntSkipList::Iterator iter(list_);
  iter.SeekToFirst();
  int idx = 0;
  while (iter.Valid()) {
    ASSERT_LT(idx, (int)values.size());
    EXPECT_EQ(values[idx], iter.key());
    iter.Next();
    idx++;
  }
  EXPECT_EQ((int)values.size(), idx);
}

// Test iterator backward traversal (Prev)
TEST_F(SkipListTest_479, IteratorBackwardTraversal_479) {
  std::vector<int> values = {50, 10, 30, 20, 40};
  for (int v : values) {
    list_->Insert(v);
  }
  std::sort(values.begin(), values.end());

  IntSkipList::Iterator iter(list_);
  iter.SeekToLast();
  int idx = (int)values.size() - 1;
  while (iter.Valid()) {
    ASSERT_GE(idx, 0);
    EXPECT_EQ(values[idx], iter.key());
    iter.Prev();
    idx--;
  }
  EXPECT_EQ(-1, idx);
}

// Test iterator Seek finds the correct position
TEST_F(SkipListTest_479, IteratorSeek_479) {
  list_->Insert(10);
  list_->Insert(20);
  list_->Insert(30);
  list_->Insert(40);
  list_->Insert(50);

  IntSkipList::Iterator iter(list_);
  
  // Seek to exact key
  iter.Seek(30);
  EXPECT_TRUE(iter.Valid());
  EXPECT_EQ(30, iter.key());
  
  // Seek to key between existing keys - should find >= key
  iter.Seek(25);
  EXPECT_TRUE(iter.Valid());
  EXPECT_EQ(30, iter.key());
  
  // Seek to key smaller than all
  iter.Seek(5);
  EXPECT_TRUE(iter.Valid());
  EXPECT_EQ(10, iter.key());
  
  // Seek to key larger than all
  iter.Seek(55);
  EXPECT_FALSE(iter.Valid());
}

