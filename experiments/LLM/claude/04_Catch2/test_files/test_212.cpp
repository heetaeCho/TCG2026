#include <gtest/gtest.h>
#include <cmath>
#include <limits>
#include <string>

// Include Catch2 matcher headers
#include "catch2/matchers/catch_matchers_floating_point.hpp"
#include "catch2/matchers/catch_matchers.hpp"

using namespace Catch::Matchers;

class IsNaNMatcherTest_212 : public ::testing::Test {
protected:
    IsNaNMatcher matcher = IsNaN();
};

// Normal operation: NaN double should match
TEST_F(IsNaNMatcherTest_212, MatchesNaNDouble_212) {
    double nanVal = std::numeric_limits<double>::quiet_NaN();
    EXPECT_TRUE(matcher.match(nanVal));
}

// Normal operation: NaN float should match
TEST_F(IsNaNMatcherTest_212, MatchesNaNFloat_212) {
    float nanVal = std::numeric_limits<float>::quiet_NaN();
    EXPECT_TRUE(matcher.match(nanVal));
}

// Normal operation: signaling NaN double should match
TEST_F(IsNaNMatcherTest_212, MatchesSignalingNaNDouble_212) {
    double snanVal = std::numeric_limits<double>::signaling_NaN();
    EXPECT_TRUE(matcher.match(snanVal));
}

// Normal operation: signaling NaN float should match
TEST_F(IsNaNMatcherTest_212, MatchesSignalingNaNFloat_212) {
    float snanVal = std::numeric_limits<float>::signaling_NaN();
    EXPECT_TRUE(matcher.match(snanVal));
}

// Boundary: zero double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchZeroDouble_212) {
    double val = 0.0;
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: zero float should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchZeroFloat_212) {
    float val = 0.0f;
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: negative zero should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchNegativeZeroDouble_212) {
    double val = -0.0;
    EXPECT_FALSE(matcher.match(val));
}

// Normal: regular positive double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchPositiveDouble_212) {
    double val = 1.5;
    EXPECT_FALSE(matcher.match(val));
}

// Normal: regular negative double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchNegativeDouble_212) {
    double val = -42.0;
    EXPECT_FALSE(matcher.match(val));
}

// Normal: regular positive float should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchPositiveFloat_212) {
    float val = 3.14f;
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: positive infinity double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchPositiveInfinityDouble_212) {
    double val = std::numeric_limits<double>::infinity();
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: negative infinity double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchNegativeInfinityDouble_212) {
    double val = -std::numeric_limits<double>::infinity();
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: positive infinity float should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchPositiveInfinityFloat_212) {
    float val = std::numeric_limits<float>::infinity();
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: negative infinity float should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchNegativeInfinityFloat_212) {
    float val = -std::numeric_limits<float>::infinity();
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: max double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchMaxDouble_212) {
    double val = std::numeric_limits<double>::max();
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: min (smallest positive) double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchMinDouble_212) {
    double val = std::numeric_limits<double>::min();
    EXPECT_FALSE(matcher.match(val));
}

// Boundary: denorm_min double should not match
TEST_F(IsNaNMatcherTest_212, DoesNotMatchDenormMinDouble_212) {
    double val = std::numeric_limits<double>::denorm_min();
    EXPECT_FALSE(matcher.match(val));
}

// Verify describe() returns a non-empty description string
TEST_F(IsNaNMatcherTest_212, DescribeReturnsNonEmptyString_212) {
    std::string description = matcher.describe();
    EXPECT_FALSE(description.empty());
}

// Verify factory function returns valid matcher
TEST_F(IsNaNMatcherTest_212, FactoryFunctionReturnsValidMatcher_212) {
    auto m = IsNaN();
    double nanVal = std::numeric_limits<double>::quiet_NaN();
    EXPECT_TRUE(m.match(nanVal));
    EXPECT_FALSE(m.match(0.0));
}

// NaN generated by arithmetic should match
TEST_F(IsNaNMatcherTest_212, MatchesArithmeticNaN_212) {
    double val = 0.0 / 0.0;
    EXPECT_TRUE(matcher.match(val));
}
