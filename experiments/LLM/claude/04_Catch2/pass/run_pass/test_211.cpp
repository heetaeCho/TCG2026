// Test that describe does not return an empty string
TEST(IsNaNMatcherTest_211, DescribeIsNotEmpty_211) {
    IsNaNMatcher matcher;
    std::string description = matcher.describe();
    EXPECT_FALSE(description.empty());
}

// Test that multiple calls to describe return the same result
TEST(IsNaNMatcherTest_211, DescribeIsConsistent_211) {
    IsNaNMatcher matcher;
    std::string desc1 = matcher.describe();
    std::string desc2 = matcher.describe();
    EXPECT_EQ(desc1, desc2);
}

// Test that multiple calls to match with the same NaN value are consistent
TEST(IsNaNMatcherTest_211, MatchIsConsistentForNaN_211) {
    IsNaNMatcher matcher;
    double nanValue = std::numeric_limits<double>::quiet_NaN();
    EXPECT_TRUE(matcher.match(nanValue));
    EXPECT_TRUE(matcher.match(nanValue));
}

// Test that multiple calls to match with a non-NaN value are consistent
TEST(IsNaNMatcherTest_211, MatchIsConsistentForNonNaN_211) {
    IsNaNMatcher matcher;
    double value = 1.0;
    EXPECT_FALSE(matcher.match(value));
    EXPECT_FALSE(matcher.match(value));
}

// Test NaN generated via invalid arithmetic operation
TEST(IsNaNMatcherTest_211, MatchReturnsTrueForArithmeticNaN_211) {
    IsNaNMatcher matcher;
    double nanValue = 0.0 / 0.0;
    EXPECT_TRUE(matcher.match(nanValue));
}
