#include <gtest/gtest.h>

#include "Decrypt.cc"



TEST(Sha256Test_1635, NormalOperation_EmptyString_1635) {

    unsigned char msg[] = "";

    int msgLen = 0;

    unsigned char hash[32] = {0};

    sha256(msg, msgLen, hash);

    unsigned char expectedHash[] = {

        0xe3, 0xb0, 0xc4, 0x42, 0x98, 0xfc, 0x1c, 0x14,

        0x9a, 0xfb, 0xf4, 0xc8, 0x99, 0x6f, 0xb9, 0x23,

        0xca, 0xf5, 0xd3, 0x90, 0x7d, 0x93, 0x90, 0x48,

        0x6a, 0xec, 0xcb, 0xc4, 0x2f, 0xe5, 0xd1, 0x6e

    };

    EXPECT_EQ(memcmp(hash, expectedHash, 32), 0);

}



