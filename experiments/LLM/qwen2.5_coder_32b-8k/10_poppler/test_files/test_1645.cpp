#include <gtest/gtest.h>

#include <gmock/gmock.h>

#include <cstring>



// Assuming sha384 function is declared in a header file, include that header.

// For this example, we will declare it here.

extern "C" void sha384(unsigned char *msg, int msgLen, unsigned char *hash);



class Sha384Test_1645 : public ::testing::Test {

protected:

    const int hashSize = 48; // SHA-384 hash size is 48 bytes

    unsigned char hash[48];

};



TEST_F(Sha384Test_1645, ZeroLengthMessage_1645) {

    unsigned char msg[] = "";

    sha384(msg, 0, hash);

    // Expected SHA-384 hash of an empty string

    unsigned char expectedHash[48] = {

        0x38, 0xb0, 0x60, 0xa7, 0x51, 0xac, 0x96, 0x38,

        0x4c, 0xd9, 0x32, 0x7e, 0xb1, 0xb1, 0xe3, 0x6a,

        0xcb, 0xca, 0xa5, 0x7b, 0x2d, 0x9d, 0xfc, 0xd8,

        0xc8, 0x8c, 0x63, 0xf8, 0x1a, 0x64, 0x7f, 0x3b,

        0xa5, 0xe2, 0xa9, 0xde, 0x6d, 0xc0, 0x1e, 0x86

    };

    EXPECT_EQ(memcmp(hash, expectedHash, hashSize), 0);

}



