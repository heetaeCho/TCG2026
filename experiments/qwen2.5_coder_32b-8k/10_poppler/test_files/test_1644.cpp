#include <gtest/gtest.h>

#include <cstring>



// Assuming sha512 function is part of a header file named "sha512.h"

extern void sha512(unsigned char *msg, int msgLen, unsigned char *hash);



class Sha512Test_1644 : public ::testing::Test {

protected:

    static constexpr size_t HASH_SIZE = 64;

};



TEST_F(Sha512Test_1644, ZeroLengthMessage_1644) {

    unsigned char msg[0] = {};

    unsigned char hash[HASH_SIZE] = {0};

    sha512(msg, 0, hash);

    

    // Expected SHA-512 hash for a zero-length message

    unsigned char expectedHash[] = {

        0xcf, 0x83, 0xe1, 0x35, 0x7f, 0xeb, 0xee, 0x9c,

        0xb4, 0x5b, 0x62, 0xd8, 0x0d, 0xed, 0xf3, 0xca,

        0xf1, 0xa7, 0xc4, 0x6a, 0x9e, 0x8c, 0xd0, 0x2b,

        0x2d, 0x5d, 0xcc, 0x30, 0xe1, 0xb1, 0xa3, 0xf1,

        0x4a, 0xfc, 0xc9, 0xc7, 0x62, 0xd4, 0x8c, 0xdc,

        0xcd, 0xe5, 0xbd, 0x7d, 0xa3, 0xb4, 0xe6, 0xcf,

        0x11, 0x2b, 0x07, 0x9d, 0x8e, 0x5c, 0xd6, 0x41

    };



    EXPECT_EQ(memcmp(hash, expectedHash, HASH_SIZE), 0);

}



