TEST(GUIDTagTest_2198, ConstructorFromBytes_NormalOperation_2198) {

    uint8_t bytes[] = {0x12, 0x34, 0x56, 0x78, 0x9A, 0xBC, 0xDE, 0xEF, 

                       0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF};

    Exiv2::AsfVideo::GUIDTag guid(bytes);



    // Verify the constructed GUID values

    EXPECT_EQ(guid.to_string(), "78563412-bc9a-deef-0123-456789abcdef");

}



TEST(GUIDTagTest_2198, ConstructorFromBytes_BoundaryConditions_2198) {

    uint8_t bytes[] = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,

                       0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00};

    Exiv2::AsfVideo::GUIDTag guid(bytes);



    EXPECT_EQ(guid.to_string(), "00000000-0000-0000-0000-000000000000");



    uint8_t bytesMax[] = {0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,

                          0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF};

    Exiv2::AsfVideo::GUIDTag guidMax(bytesMax);



    EXPECT_EQ(guidMax.to_string(), "ffffffff-ffff-ffff-ffff-ffffffffffff");

}



